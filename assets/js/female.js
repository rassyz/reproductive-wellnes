// kalender mens
const calendar = document.getElementById("calendar");
const result = document.getElementById("result");
let selectedDates = []; // Store marked dates

// Create days of the month
for (let i = 1; i <= 31; i++) {
  const day = document.createElement("div");
  day.classList.add("day");
  day.textContent = i;

  // Add click event for marking days
  day.addEventListener("click", () => {
    if (day.classList.contains("marked")) {
      day.classList.remove("marked");
      selectedDates = selectedDates.filter(date => date !== i);
    } else {
      day.classList.add("marked");
      selectedDates.push(i);
    }
  });

  calendar.appendChild(day);
}

// Calculate cycle and determine normal/abnormal
document.getElementById("calculate").addEventListener("click", () => {
  if (selectedDates.length < 2) {
    result.textContent = "Tandai setidaknya dua tanggal untuk menghitung durasi menstruasi.";
    return;
  }

  // Sort dates and calculate duration and cycle
  selectedDates.sort((a, b) => a - b);
  const duration = selectedDates[selectedDates.length - 1] - selectedDates[0] + 1;

  // Determine normal/abnormal
  let message = `Durasi menstruasi: ${duration} hari. `;
  if (duration < 3 || duration > 7) {
    message += "Durasi menstruasi tidak normal. ";
  } else {
    message += "Durasi menstruasi normal. ";
  }

  result.textContent = message;
});
