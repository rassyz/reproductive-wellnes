// search logic
const searchInput = document.querySelector('.search input');
const searchButton = document.querySelector('.search button');
const tabs = document.querySelectorAll('.tab-pane');
const navLinks = document.querySelectorAll('.nav-link');

// Fungsi untuk menampilkan custom popup
function popup(message) {
  let popupDiv = document.querySelector('.custom-popup');
  if (!popupDiv) {
    popupDiv = document.createElement('div');
    popupDiv.classList.add('custom-popup');

    const popupContent = document.createElement('div');
    popupContent.classList.add('popup-content');

    const popupText = document.createElement('p');
    popupText.classList.add('popup-text');

    const closeButton = document.createElement('button');
    closeButton.classList.add('popup-close');
    closeButton.textContent = 'Tutup';

    closeButton.addEventListener('click', () => {
      popupDiv.style.display = 'none';
    });

    popupContent.appendChild(popupText);
    popupContent.appendChild(closeButton);
    popupDiv.appendChild(popupContent);
    document.body.appendChild(popupDiv);
  }

  // Tampilkan pop-up dengan pesan baru
  popupDiv.querySelector('.popup-text').textContent = message;
  popupDiv.style.display = 'flex';
}

// Fungsi untuk mencari dan menampilkan materi
function searchMaterial() {
  const query = searchInput.value.trim().toLowerCase();

  // Jika input kosong, hentikan proses
  if (!query) {
    popup('Silahkan masukkan kata kunci untuk mencari.');
    return;
  }

  let found = false;

  // Tangani pencarian khusus berdasarkan query
  const displays = {
    'kalender menstruasi': kalenderDisplay,
    'menstruasi': kalenderDisplay,
    'gejala kehamilan': hamilDisplay,
    'kehamilan': hamilDisplay,
    'pengertian': pengDisplay,
    'definisi': pengDisplay,
    'cara menjaga kebersihan vagina': vagDisplay,
    'menjaga kebersihan vagina': vagDisplay,
    'kebersihan vagina': vagDisplay,
    'macam macam penyakit vagina': sakitDisplay,
    'penyakit vagina': sakitDisplay,
    'cara pencegahan penyakit': cegahDisplay,
    'pencegahan penyakit': cegahDisplay,
    'pengobatan': obatDisplay,
    'macam macam kb': kbDisplay,
    'kb': kbDisplay,
    'penyimpangan seksual': nyimpangDisplay,
    'siklus haid': haidDisplay,
    'haid': haidDisplay,
    'dampak': dampakDisplay,
    'nutrisi': nutrisiDisplay,
    'olahraga': nutrisiDisplay,
  };

  if (displays[query]) {
    tabs.forEach((t) => t.classList.remove('active', 'show'));
    navLinks.forEach((link) => link.classList.remove('active', 'show'));

    displays[query].classList.add('active', 'show');
    document.querySelector(`[href="#${displays[query].id}"]`).classList.add('active', 'show');

    found = true;
  }

  // Loop melalui setiap tab dan periksa apakah ID cocok dengan query
  tabs.forEach((tab) => {
    if (tab.id.toLowerCase().includes(query)) {
      tabs.forEach((t) => t.classList.remove('active', 'show'));
      navLinks.forEach((link) => link.classList.remove('active', 'show'));

      tab.classList.add('active', 'show');
      document.querySelector(`[href="#${tab.id}"]`).classList.add('active', 'show');

      found = true;
    }
  });

  if (!found) {
    popup('Materi tidak ditemukan. Coba kata kunci lain.');
  }
}

// Tambahkan event listener pada tombol search
searchButton.addEventListener('click', searchMaterial);

// Tambahkan event listener pada input untuk pencarian saat tekan Enter
searchInput.addEventListener('keypress', (e) => {
  if (e.key === 'Enter') {
    searchMaterial();
  }
});

